#' twotothepowerof
#'
#' A function to find two to the power of a number
#'
#' @param a a numeric value
#' @return two to the power of a
#' @export
#' @examples 
#' twotothepowerof(3)
#' @docType package
#' @name twotothepowerof
#' @author John Merrall, School of Geography and Earth Sciences, McMaster University \email{merralja@@mcmaster.ca}
#' @references \url{https://github.com/merralja/SchoolProbit}

twotothepowerof <- function(a){
    2^a
}
